def wuxing_role_names(me_wx):
    """回傳以某五行為日主時，五種十神類別對應的五行"""
    SHENG = {"木": "火", "火": "土", "土": "金", "金": "水", "水": "木"}
    KE = {"木": "土", "土": "水", "水": "火", "火": "金", "金": "木"}
    sheng_me = [k for k, v in SHENG.items() if v == me_wx][0]
    wo_sheng = SHENG[me_wx]
    wo_ke = KE[me_wx]
    ke_me = [k for k, v in KE.items() if v == me_wx][0]
    return {
        "比劫(同類)": me_wx,
        "印星(生我)": sheng_me,
        "食傷(我生)": wo_sheng,
        "財星(我剋)": wo_ke,
        "官殺(剋我)": ke_me,
    }


def plain_reading(analysis):
    """產生一段白話解讀（list of 段落）"""
    dm = analysis["day_master"]
    wx = analysis["day_master_wx"]
    st = analysis["strength"]
    yj = analysis["yong_ji"]
    strong = st["is_strong"]
    yong = yj["yong"]
    ji = yj["ji"]

    paras = []

    # 1. 你是誰
    metaphor = {
        "水": "一灣流動的水", "木": "一株生長的樹", "火": "一團燃燒的火",
        "土": "一片承載的大地", "金": "一塊待琢的金屬",
    }[wx]
    paras.append(
        f"你的日主是「{dm}（{wx}）」，代表你這個人。可以想像你像{metaphor}。"
        f"整張命盤，就是在看金木水火土這五種能量，圍繞著你這個「{wx}」是幫你、還是耗你。"
    )

    # 2. 身強身弱
    if strong:
        paras.append(
            f"你的體性是「身強」——代表你本身的能量偏旺、幫你的力量夠多。"
            f"身強的人不怕壓力，反而需要把多餘的能量「用出去、消耗掉」才會平衡，"
            f"所以你喜歡的是能讓你發揮、賺錢、扛責任的能量。"
        )
    else:
        paras.append(
            f"你的體性是「身弱」——代表你本身的能量偏弱、容易被周圍的力量消耗。"
            f"這不是壞事，只是表示你需要「被幫扶、被滋養」才會順。"
            f"就像{metaphor}，需要有東西來補充、支撐你，而不是一直被消耗。"
        )

    # 3. 喜用神/忌神 白話
    yong_str = "、".join(yong)
    ji_str = "、".join(ji)
    paras.append(
        f"你的「喜用神」是 {yong_str}——這是對你有幫助、能讓你平衡順遂的能量。"
        f"你的「忌神」是 {ji_str}——這是會消耗你、給你壓力的能量。"
    )

    # 4. 實務建議
    tips = []
    color_map = {"金":"白色、金色、銀色","木":"綠色、青色","水":"黑色、藍色","火":"紅色、紫色","土":"黃色、棕色"}
    industry_map = {
        "金":"金融、科技硬體、機械、法律",
        "木":"教育、出版、醫護、文創",
        "水":"資訊、貿易、傳播、物流",
        "火":"能源、餐飲、電子、演藝",
        "土":"房地產、營建、管理、仲介",
    }
    yong_colors = "、".join(color_map[y] for y in yong)
    yong_ind = "、".join(industry_map[y] for y in yong)
    paras.append(
        f"所以傳統上會建議：你可以多親近 {yong_str} 的能量——"
        f"例如常用的顏色可選 {yong_colors}；"
        f"若論行業方向，{yong_str} 對應的領域（如 {yong_ind}）相對合你。"
        f"反之，{ji_str} 當旺的年份（後面流年表會標出來），凡事多留意、別衝動。"
    )

    # 5. 免責
    paras.append(
        "＊以上是傳統命理的框架推演，當作「更認識自己、心理上有個參考」就好。"
        "真正決定人生的，是你的選擇與努力，不是命盤。"
    )
    return paras


